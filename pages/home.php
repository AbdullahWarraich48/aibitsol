// Schedule Meet Modal Functionality
const DEFAULT_ORG_EMAIL = 'info@aibitsol.com';
let selectedDuration = 30;

function openScheduleMeetModal() {
    const modal = document.getElementById('scheduleMeetModal');
    if (!modal) return;
    
    modal.classList.remove('hidden');
    modal.classList.add('flex');
    document.documentElement.classList.add('modal-open');
    document.body.style.overflow = 'hidden';
    
    // Set default date to today
    const today = new Date();
    const dateString = today.toISOString().split('T')[0];
    const dateInput = document.getElementById('meetDate');
    if (dateInput) dateInput.value = dateString;
    
    // Set default time to current time + 1 hour
    const oneHourLater = new Date(today.getTime() + 60 * 60 * 1000);
    const timeString = oneHourLater.toTimeString().slice(0, 5);
    const timeInput = document.getElementById('meetTime');
    if (timeInput) timeInput.value = timeString;
    
    // Reset form
    const descInput = document.getElementById('meetDescription');
    if (descInput) descInput.value = 'Discussion about project';
    
    const emailInput = document.getElementById('meetEmails');
    const emailError = document.getElementById('emailError');
    if (emailInput) {
        emailInput.value = '';
        emailInput.classList.remove('error');
    }
    if (emailError) {
        emailError.classList.add('hidden');
        emailError.textContent = '';
    }
    
    // Ensure form handler is attached (use onclick on button as backup)
    const submitButton = document.getElementById('scheduleMeetSubmit');
    if (submitButton) {
        submitButton.onclick = function(e) {
            e.preventDefault();
            e.stopPropagation();
            handleScheduleSubmit(e);
            return false;
        };
    }
}

function closeScheduleMeetModal() {
    const modal = document.getElementById('scheduleMeetModal');
    if (!modal) return;
    
    modal.classList.add('hidden');
    modal.classList.remove('flex');
    document.documentElement.classList.remove('modal-open');
    document.body.style.overflow = '';
}

// Initialize modal functionality
function initializeScheduleMeetModal() {
    // Check if modal exists before initializing
    const modal = document.getElementById('scheduleMeetModal');
    if (!modal) {
        // Modal doesn't exist on this page, skip initialization
        return;
    }
    
    // Duration button handlers
    const durationButtons = document.querySelectorAll('.duration-btn');
    if (durationButtons && durationButtons.length > 0) {
        durationButtons.forEach(btn => {
            btn.addEventListener('click', function() {
                selectedDuration = parseInt(this.getAttribute('data-duration'));
                durationButtons.forEach(b => {
                    b.classList.remove('bg-indigo-600', 'text-white');
                    b.classList.add('bg-slate-100', 'text-slate-700');
                });
                this.classList.remove('bg-slate-100', 'text-slate-700');
                this.classList.add('bg-indigo-600', 'text-white');
            });
        });
    }
    
    // Email validation
    const emailInput = document.getElementById('meetEmails');
    if (emailInput) {
        emailInput.addEventListener('input', function() {
            validateEmails(this.value);
        });
    }
    
    // Form submission handler
    const form = document.getElementById('scheduleMeetForm');
    if (form) {
        form.addEventListener('submit', function(e) {
            e.preventDefault();
            e.stopPropagation();
            handleScheduleSubmit(e);
            return false;
        }, false);
    }
    
    // Also attach to submit button directly as backup
    const submitButton = document.getElementById('scheduleMeetSubmit');
    if (submitButton) {
        submitButton.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();
            handleScheduleSubmit(e);
            return false;
        }, false);
    }
    
    // Close modal on backdrop click
    if (modal) {
        modal.addEventListener('click', function(e) {
            if (e.target === modal) {
                closeScheduleMeetModal();
            }
        });
    }
}

// Initialize when DOM is ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initializeScheduleMeetModal);
} else {
    // DOM is already loaded
    initializeScheduleMeetModal();
}

function validateEmailPattern(email) {
    const emailRegex = /^[a-zA-Z0-9.!#$%&'*+/=?^_`{|}~-]+@[a-zA-Z0-9](?:[a-zA-Z0-9-]{0,61}[a-zA-Z0-9])?(?:\.[a-zA-Z0-9](?:[a-zA-Z0-9-]{0,61}[a-zA-Z0-9])?)*$/;
    return emailRegex.test(email.trim());
}

function validateEmails(emailString) {
    const errorElement = document.getElementById('emailError');
    const emailInput = document.getElementById('meetEmails');
    if (!errorElement || !emailInput) return { isValid: true, invalidEmails: [], validEmails: [] };
    
    if (emailString.trim() === "") {
        errorElement.classList.add('hidden');
        errorElement.textContent = '';
        emailInput.classList.remove('error');
        return { isValid: true, invalidEmails: [], validEmails: [] };
    }
    
    const emailList = emailString.split(',').map(email => email.trim()).filter(Boolean);
    const invalidEmails = [];
    const validEmails = [];
    
    emailList.forEach(email => {
        if (validateEmailPattern(email)) {
            validEmails.push(email);
        } else {
            invalidEmails.push(email);
        }
    });
    
    if (invalidEmails.length > 0) {
        errorElement.classList.remove('hidden');
        errorElement.textContent = `Invalid email format: ${invalidEmails.join(', ')}`;
        emailInput.classList.add('error');
        return { isValid: false, invalidEmails, validEmails };
    } else {
        errorElement.classList.add('hidden');
        errorElement.textContent = '';
        emailInput.classList.remove('error');
        return { isValid: true, invalidEmails: [], validEmails };
    }
}

function formatDate(datetimeStr) {
    const date = new Date(datetimeStr);
    return date.toISOString().replace(/[-:]/g, "").split(".")[0] + "Z";
}

function handleScheduleSubmit(event) {
    if (event) {
        event.preventDefault();
        event.stopPropagation();
    }
    
    try {
        const dateInput = document.getElementById('meetDate');
        const timeInput = document.getElementById('meetTime');
        const timezoneInput = document.getElementById('meetTimezone');
        const descriptionInput = document.getElementById('meetDescription');
        const emailsInput = document.getElementById('meetEmails');
        
        if (!dateInput || !timeInput || !timezoneInput || !descriptionInput || !emailsInput) {
            console.error('Form elements not found');
            alert('Error: Form elements not found. Please refresh the page and try again.');
            return;
        }
        
        const date = dateInput.value;
        const time = timeInput.value;
        const timezone = timezoneInput.value;
        const description = descriptionInput.value;
        const emails = emailsInput.value;
        
        if (!date || !time) {
            alert("Please select both date and time for the meeting.");
            return;
        }
        
        if (!emails || emails.trim() === '') {
            alert("Please enter at least one email address.");
            return;
        }
        
        // Validate emails
        const validation = validateEmails(emails);
        if (!validation.isValid) {
            alert(`Invalid email addresses: ${validation.invalidEmails.join(', ')}`);
            return;
        }
        
        // Create datetime string
        const datetimeString = `${date}T${time}`;
        const startTime = new Date(datetimeString);
        
        if (isNaN(startTime.getTime())) {
            alert("Invalid date or time selected. Please check your selections.");
            return;
        }
        
        const endTime = new Date(startTime.getTime() + selectedDuration * 60000);
        
        // Combine user and company emails
        const normalizedOrgEmail = DEFAULT_ORG_EMAIL.trim();
        const inputEmails = emails.replace(/\s+/g, "");
        const allEmails = [inputEmails, normalizedOrgEmail].filter(Boolean).join(",");
        
        // Get timezone label
        const timezoneSelect = document.getElementById('meetTimezone');
        const timezoneInfo = timezoneSelect ? timezoneSelect.options[timezoneSelect.selectedIndex].text : timezone;
        
        const formattedStart = formatDate(startTime.toISOString());
        const formattedEnd = formatDate(endTime.toISOString());
        
        // Detailed meeting description
        const detailedDescription = `${description}

Company Contact: ${normalizedOrgEmail}
Timezone: ${timezoneInfo}

Meeting Participants:
${allEmails.split(',').map(email => `• ${email.trim()}`).join('\n')}

Google Meet will be automatically added when you save the event.`;
        
        // Build the Google Calendar event creation URL
        const baseUrl = "https://calendar.google.com/calendar/u/0/r/eventedit";
        const params = new URLSearchParams({
            text: "Scheduled Meeting",
            details: detailedDescription,
            dates: `${formattedStart}/${formattedEnd}`,
            add: allEmails,
            ctz: timezone,
        });
        
        const calendarLink = `${baseUrl}?${params.toString()}`;
        
        // Open Google Calendar with pre-filled event data
        window.open(calendarLink, "_blank");
        
        // Close modal
        closeScheduleMeetModal();
    } catch (error) {
        console.error('Error in handleScheduleSubmit:', error);
        alert('An error occurred while scheduling the meeting. Please try again.');
    }
}

// Wrapper function for inline onclick
function submitScheduleMeetForm(e) {
    if (e) {
        e.preventDefault();
        e.stopPropagation();
    }
    if (typeof handleScheduleSubmit === 'function') {
        handleScheduleSubmit(e);
    }
    return false;
}

